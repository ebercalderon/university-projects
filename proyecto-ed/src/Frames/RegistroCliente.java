    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

            PreparedStatement pps;
        try {
            pps = cn.prepareStatement("INSERT INTO clientes(usuario, password, nombres, apellidos, dni, direccion) VALUES(?,?,?,?,?,?)");
            pps.setString(1, jTextField1.getText());
            pps.setString(2, jPasswordField1.getText());
            pps.setString(3, jTextField3.getText());
            pps.setString(4, jTextField4.getText());
            pps.setString(5, jTextField5.getText());
            pps.setString(6, jTextField6.getText());
            pps.executeUpdate();
            jTextField1.setText("");
            jPasswordField1.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");

            JOptionPane.showMessageDialog(null, "Datos guardados");
        } catch (SQLException ex) {
            Logger.getLogger(RegistroCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }//GEN-LAST:event_jButton1ActionPerformed

