    public void suma(){
        int fila = jTable1.getRowCount();
        int i;
        float suma = 0;
        for(i=0; i<fila; i++){
            float sum = Float.valueOf((String) jTable1.getValueAt(i, 2));
            suma += sum;
        }
        jTextField1.setText(String.valueOf(suma));
    }
   
    
